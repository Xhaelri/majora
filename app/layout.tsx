import type { Metadata } from "next";
import { Corinthia, Libre_Franklin, Meow_Script } from "next/font/google";
import "./globals.css";
import ThemeContextProvider from "../context/Theme";
import Header from "@/components/Header/Header";
import Footer from "@/components/Footer/Footer";
import { Providers } from "@/redux/storeProvider";
import { Toaster } from "@/components/ui/sonner";
import { SessionProvider } from "next-auth/react";
import { auth } from "@/auth";
import { CartProvider } from "@/context/CartContext";

const libre = Libre_Franklin({
  subsets: ["latin"],
  weight: ["100", "200", "300", "400", "500", "600", "700", "800", "900"],
  variable: "--font-libre-franklin",
  display: "swap",
});

const corinthia = Meow_Script({
  subsets: ["latin"],
  weight: ["400"],
  variable: "--font-corinthia",
  display: "swap",
});

export const metadata: Metadata = {
  title: "SEKRA",
  description: "Generated by create next app",
};

export default async function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  const session = await auth();

  return (
    <html lang="en">
      <body
        className={`${libre.variable} ${corinthia.variable} ${libre.className} antialiased`}
      >
        <SessionProvider session={session}>
          <CartProvider>
            <Providers>
              <ThemeContextProvider>
                <Header />
                <Toaster
                  position="top-right"
                  expand={false}
                  offset={60}
                  mobileOffset={60}
                  duration={2000}
                  invert
                  theme="light"
                />
                {children}
                <Footer />
              </ThemeContextProvider>
            </Providers>
          </CartProvider>
        </SessionProvider>
      </body>
    </html>
  );
}
